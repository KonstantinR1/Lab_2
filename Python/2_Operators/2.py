b += 2      # b = b + 2
c -= 3      # c = c - 3
d *= 4      # b = b * 4
e /= 5      # e = e / 5
f %= 6      # f = f % 6
g //= 7     # g = g // 7
h **= 8     # h = h ** 8
i &= 9      # i = i & 9
j |= 10     # j = j | 10
k ^= 11     # k = k ^ 11
l >>= 12    # l = l >> 12
m <<= 13    # m = m << 13

