# Comparison Operators

a = 6
b = 19

print(1, a == b)    # Equal                    1
print(2, a != b)    # Not equal                2
print(3, a > b)     # Greator than             3
print(4, a < b)     # Less than                4
print(5, a >= b)    # Greator than or equal to 5
print(6, a <= b)    # Less than or equal to    6

# Output will be in boolean condition